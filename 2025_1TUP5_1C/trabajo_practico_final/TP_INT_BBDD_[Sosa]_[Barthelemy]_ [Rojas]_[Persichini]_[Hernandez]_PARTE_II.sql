CREATE DATABASE tp_bdd;
USE tp_bdd;

CREATE TABLE usuario (
    id_user INTEGER AUTO_INCREMENT PRIMARY KEY,
    clave VARCHAR(50),
    mail VARCHAR(50),
    nombre_de_usuario VARCHAR(25),
    genero VARCHAR(20),
    fecha_nacimiento DATE
);

CREATE TABLE perfil_Usuario (
    id_perfil INTEGER AUTO_INCREMENT PRIMARY KEY,
    edad INTEGER,
    apodo VARCHAR(100),
    nacionalidad VARCHAR(100),
    id_user INTEGER,
    intereses VARCHAR(100),
    nombre VARCHAR(100),
    apellido VARCHAR(100),
    FOREIGN KEY (id_user) REFERENCES Usuario(id_user)
);

CREATE TABLE seguidores (
    id_seguimiento INTEGER AUTO_INCREMENT PRIMARY KEY,
    id_user INTEGER,
    id_follower INTEGER,
    fecha_inicio DATE,
    FOREIGN KEY (id_user) REFERENCES Usuario(id_user),
    FOREIGN KEY (id_follower) REFERENCES Usuario(id_user)
);

CREATE TABLE actividades (
    id_actividad INTEGER AUTO_INCREMENT PRIMARY KEY,
    nombre_acti VARCHAR(100),
    id_user INTEGER,
    primera_vez DATE,
    last_join DATE,
    fecha_inicio DATE,
    fecha_final DATE,
    FOREIGN KEY (id_user) REFERENCES Usuario(id_user)
);

CREATE TABLE grupos (
    id_grupo INTEGER AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100),
    fecha_union DATE,
    id_user INTEGER,
    FOREIGN KEY (id_user) REFERENCES Usuario(id_user)
);

CREATE TABLE amigos (
    id_amistad INTEGER AUTO_INCREMENT PRIMARY KEY,
    id_user INTEGER,
    id_friend INTEGER,
    fecha_inicio DATE,
    FOREIGN KEY (id_user) REFERENCES Usuario(id_user),
    FOREIGN KEY (id_friend) REFERENCES Usuario(id_user)
);

CREATE TABLE publicacion (
    id_publicacion INTEGER AUTO_INCREMENT PRIMARY KEY,
    id_interaccion INTEGER,
    texto VARCHAR(1000),
    img VARCHAR(1000)
);

CREATE TABLE interaccion (
    id_interaccion INTEGER AUTO_INCREMENT PRIMARY KEY,
    id_publicacion INTEGER,
    id_user INTEGER,
    fecha DATE,
    FOREIGN KEY (id_publicacion) REFERENCES Publicacion(id_publicacion),
    FOREIGN KEY (id_user) REFERENCES Usuario(id_user)
);

ALTER TABLE publicacion ADD FOREIGN KEY (id_interaccion) REFERENCES Interaccion(id_interaccion);

CREATE TABLE me_gusta (
    id_megusta INTEGER AUTO_INCREMENT PRIMARY KEY,
    id_interaccion INTEGER,
    cantidad INTEGER,
    id_user INTEGER,
    FOREIGN KEY (id_interaccion) REFERENCES Interaccion(id_interaccion),
    FOREIGN KEY (id_user) REFERENCES Usuario(id_user)
);

CREATE TABLE comentario (
    id_comentario INTEGER AUTO_INCREMENT PRIMARY KEY,
    id_interaccion INTEGER,
    texto VARCHAR(1000),
    id_user INTEGER,
    FOREIGN KEY (id_interaccion) REFERENCES Interaccion(id_interaccion),
    FOREIGN KEY (id_user) REFERENCES Usuario(id_user)
);

