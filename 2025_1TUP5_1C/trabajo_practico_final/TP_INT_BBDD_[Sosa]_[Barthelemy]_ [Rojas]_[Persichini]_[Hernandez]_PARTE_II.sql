-- utilizamos dos  INNER JOIN para traer las publicaciones donde hubo texto.
SELECT 
publicacion.id_publicacion AS id_publicacion,
interaccion.fecha,
comentario.texto
FROM publicacion INNER JOIN interaccion ON publicacion.id_interaccion = interaccion.id_interaccion
INNER JOIN comentario ON interaccion.id_interaccion = comentario.id_interaccion;
-- filtra cantidad de comentarios por genero
SELECT 
usuario.genero AS genero_usuario,
COUNT(*) AS cantidad_comentarios

FROM comentario INNER JOIN usuario ON comentario.id_user = usuario.id_user

GROUP BY usuario.genero;
-- 3b) 

-- Muestra usuarios que no tienen amigos
SELECT usuario.id_user,amigos.id_amistad
FROM usuario LEFT JOIN amigos ON usuario.id_user = amigos.id_user
WHERE amigos.id_amistad IS NULL;

-- Muestra los usuarios y la cantidad de me gusta que dieron
SELECT 
usuario.id_user AS ID_usuario,
usuario.nombre_de_usuario AS Nombre_Usiario,
COUNT(me_gusta.id_megusta) AS Cantidad_MeGusta
FROM usuario LEFT JOIN me_gusta ON usuario.id_user = me_gusta.id_user
GROUP BY usuario.id_user, usuario.nombre_de_usuario;
-- ejercicio 3 c --
-- Muestra Cantidad de publicaciones por usuario--
SELECT 
    u.nombre_de_usuario,
    COUNT(p.id_publicacion) AS cantidad_publicaciones
FROM publicacion p
JOIN interaccion i ON p.id_interaccion = i.id_interaccion
JOIN usuario u ON i.id_user = u.id_user
GROUP BY u.id_user, u.nombre_de_usuario;
-- Muestra Usuarios con al menos 1 amigo --
SELECT 
    u.nombre_de_usuario,
    COUNT(a.id_friend) AS cantidad_amigos
FROM usuario u
