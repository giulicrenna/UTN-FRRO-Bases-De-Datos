-- Muestra el nombre del pasajero, modelo del avión, aerolínea y nombre del piloto del vuelo reservado
SELECT 
    pa.nombre AS nombre_pasajero,
    pa.apellido AS apellido_pasajero,
    av.modelo AS modelo_avion,
    al.nombre AS aerolinea,
    pi.nombre AS nombre_piloto,
    pi.apellido AS apellido_piloto
FROM reservas r
INNER JOIN pasajeros pa ON r.id_pasajero = pa.id_pasajero
INNER JOIN vuelos v ON r.id_vuelo = v.id_vuelo
INNER JOIN aviones av ON v.id_avion = av.id_avion
INNER JOIN aerolineas al ON av.id_aerolinea = al.id_aerolinea
INNER JOIN pilotos pi ON v.id_piloto = pi.id_piloto;

-- Muestra todos los pasajeros y sus reservas si las tienen; si no tienen reserva, muestra NULL en datos de reserva
SELECT 
    p.id_pasajero,
    p.nombre,
    p.apellido,
    r.id_reserva,
    r.estado
FROM pasajeros p
LEFT JOIN reservas r ON p.id_pasajero = r.id_pasajero;

-- Muestra todos los vuelos y las reservas asociadas si existen; si no hay reservas para un vuelo, muestra NULL en datos de reserva
SELECT 
    v.id_vuelo,
    v.numero_vuelo,
    v.fecha_salida,
    r.id_reserva,
    r.estado
FROM vuelos v
LEFT JOIN reservas r ON v.id_vuelo = r.id_vuelo;

-- Cuenta cantidad de reservas por estado (Confirmada, Pendiente, Cancelada)
SELECT 
    estado,
    COUNT(*) AS cantidad_reservas
FROM reservas
GROUP BY estado;

-- Suma el costo total por clase de vuelo y mostrar solo clases con más de 5 reservas
SELECT 
    clase,
    SUM(costo_total) AS suma_costo_total,
    COUNT(*) AS cantidad_reservas
FROM reservas
GROUP BY clase
HAVING COUNT(*) > 5;

-- Muestra pasajeros que tienen reservas en vuelos con costo total mayor al promedio de todas las reservas
SELECT 
    p.nombre,
    p.apellido,
    r.costo_total
FROM pasajeros p
INNER JOIN reservas r ON p.id_pasajero = r.id_pasajero
WHERE r.costo_total > (
    SELECT AVG(costo_total) FROM reservas
);

-- Muestra vuelos que tengan 1 o mas reservas confirmadas
SELECT 
    v.numero_vuelo,
    v.fecha_salida
FROM vuelos v
WHERE (
    SELECT COUNT(*)
    FROM reservas r
    WHERE r.id_vuelo = v.id_vuelo AND r.estado = 'Confirmada'
) >= 1;

