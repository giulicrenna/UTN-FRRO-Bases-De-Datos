-- Procedimiento: 
-- Actualiza el estado de una reserva a través de su ID.

CREATE PROCEDURE actualizar_estado_reserva (
    IN p_id_reserva INT, -- aca se pasa el ID de la reserva a actualizar
    IN p_nuevo_estado VARCHAR(20) -- se pasa el nuevo estado (cancelado - pendiente - confirmado)
)
BEGIN
    UPDATE reservas -- se actualiza la tabla reservas
    SET estado = p_nuevo_estado -- se guarda el nuevo estado en "estado"
    WHERE id_reserva = p_id_reserva; -- busca que coincida el id con el id ingresado
END //
