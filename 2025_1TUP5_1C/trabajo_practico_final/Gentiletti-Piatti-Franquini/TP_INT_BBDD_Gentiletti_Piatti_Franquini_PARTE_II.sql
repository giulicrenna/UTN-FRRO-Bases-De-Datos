-- Procedimiento:
-- Inserta una nueva reserva para un pasajero y vuelo dado, con datos de clase, costo, asiento y estado.

CREATE PROCEDURE insertar_reserva (
    IN p_id_pasajero INT, 
    IN p_id_vuelo INT,
    IN p_clase VARCHAR(20),
    IN p_costo DECIMAL(10,2),
    IN p_asiento VARCHAR(5),
    IN p_estado VARCHAR(20) -- recibe toda los parametros para la nueva reserva
)
BEGIN
    INSERT INTO reservas (id_pasajero, id_vuelo, fecha_reserva, clase, costo_total, asiento, estado) 
    VALUES (p_id_pasajero, p_id_vuelo, CURDATE(), p_clase, p_costo, p_asiento, p_estado); 
    -- usamos CURDATE() para que guarde la fecha actual
END //
