-- Procedimiento: cancelar_vuelo
-- Cancela un vuelo y todas sus reservas asociadas.

CREATE PROCEDURE cancelar_vuelo (
    IN p_id_vuelo INT
)
BEGIN
    -- Actualiza el estado del vuelo a 'Cancelado'
    UPDATE vuelos
    SET estado = 'Cancelado'
    WHERE id_vuelo = p_id_vuelo;

    -- Actualiza el estado de las reservas de ese vuelo a 'Cancelada'
    UPDATE reservas
    SET estado = 'Cancelada'
    WHERE id_vuelo = p_id_vuelo AND estado <> 'Cancelada';
END //
